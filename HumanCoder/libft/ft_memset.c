#include "libft.h"

void	*ft_memset(void *src, int c, size_t n)
{
	size_t	i;
	char	*str;

	str = (char *)src;
	i = 0;
	if (n)
	{
		while (i != n)
		{
			str[i] = (char)c;
			i++;
		}
	}
	return (src);
}
